/*
 * 函数名：pu8printf
 * 描述  ：串口打印数组
 * 输入  ：name字符串文本名, pbuf待打印数组 , count数组宽度 ，Mode显示模式
 * 返回  ：
 */    
void pu8printf(char* name, uint8_t* pbuf, uint32_t count, uint8_t Mode)
{
  uint32_t index;
  printf("%s:\r\n", name);
  for(index = 0; index < count; index++)
  {
    if(0 == Mode)
    {
      printf("%02d ", pbuf[index]);
    }
    else if(1 == Mode)
    {
      printf("%02X ", pbuf[index]);
    }
    else if(2 == Mode)
    {
      //  printf("%s ", pbuf[index]);
    }
  }
  printf("\r\n");
}
/*
函数功能：串口1DMA数据发送
函数形参：Sendbuff ：缓冲数据
          Bufflens ：数据长度
函数返回值：数据长度
备注：无
*/
uint16_t Usart1_DMA_Sent(uint8_t * Sendbuff, uint16_t Bufflens)
{
	uint16_t l_val = Bufflens > USART_BUFFSIZE ? USART_BUFFSIZE : Bufflens;
	if(Bufflens <= 0)
	{
		return 0;
	}
//	while(__HAL_DMA_GET_COUNTER(&hdma_usart1_tx));  //检测DMA发送通道内还有没有数据
	if(Sendbuff)
	{
		memcpy( ( void * ) Usart1_Str.Usart_SentBuff, ( const void * )Sendbuff, l_val);
	}
	HAL_UART_Transmit_DMA(&huart1, Usart1_Str.Usart_SentBuff, l_val);
  return l_val;
}

