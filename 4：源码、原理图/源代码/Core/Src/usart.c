void Usart_RecvData(void)
{
  if(Usart1_Str.RecvFlags == 1)
  { 
    ANO_DT_Data_Receive_Anl(Usart1_Str.Usart_RecvBuff , Usart1_Str.Usart_RecvLens);   //进行数据解析
    memset (Usart1_Str.Usart_RecvBuff , 0,Usart1_Str.Usart_RecvLens);                 //缓存数据清除
    Usart1_Str.Usart_RecvLens = 0;                                                    //接收的数据长度清除
    Usart1_Str.RecvFlags = 0;                                                         //标志位清除
  }
}


