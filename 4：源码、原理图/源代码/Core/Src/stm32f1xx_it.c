/**
  * @brief This function handles USART1 global interrupt.
  */
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */

  if(__HAL_UART_GET_FLAG(&huart1, UART_FLAG_IDLE) != RESET)
	{

    // 通过DMA接收指针计算接收的字节数,读取NDTR寄存器 获取DMA中未传输的数据个数,
    // 总计数减去未传输的数据个数，得到已经接收的数据个数
		Usart1_Str.Usart_RecvLens = USART_BUFFSIZE - DMA1_Channel5->CNDTR;
    
    // 失能DMA接收
    HAL_UART_DMAStop(&huart1);

//    printf("\r\nUsart_RecvLens:%d\r\n",Usart1_Str.Usart_RecvLens);
    
    Usart1_Str.RecvFlags = 1;
//    Usart1_DMA_Sent(Usart1_Str.Usart_RecvBuff,Usart1_Str.Usart_RecvLens);

    // 重新开启DMA接收
    HAL_UART_Receive_DMA(&huart1, Usart1_Str.Usart_RecvBuff, USART_BUFFSIZE); 
		__HAL_UART_CLEAR_IDLEFLAG(&huart1);
	}

  /* USER CODE END USART1_IRQn 0 */
  HAL_UART_IRQHandler(&huart1);
  /* USER CODE BEGIN USART1_IRQn 1 */

  /* USER CODE END USART1_IRQn 1 */
}

